def dialectize_text(text, dialect_key, typo_prob=0.3):
    """
    تحويل النص الفصيح إلى لهجة سعودية محددة.

    1. يستبدل الكلمات الفصحى بمرادفاتها العامية حسب اللهجة
    2. يطبق أخطاء مطبعية سعودية بنسبة typo_prob
    3. يزيل علامات الترقيم الرسمية

    المُدخلات:
        text: النص المراد تحويله
        dialect_key: مفتاح اللهجة (مثل 'najdi_riyadh', 'hijazi_jeddah')
        typo_prob: احتمال تطبيق الأخطاء المطبعية (0.0 - 1.0)

    المُخرجات:
        النص بعد التحويل
    """
    # الخطوة 1: استبدال الكلمات حسب اللهجة
    word_map = DIALECT_WORD_MAP.get(dialect_key, {})
    for formal, dialectal in word_map.items():
        text = text.replace(formal, dialectal)

    # الخطوة 2: تطبيق الأخطاء المطبعية
    if random.random() < typo_prob:
        text = apply_saudi_typos(text, intensity='normal')

    # الخطوة 3: تجريد علامات الترقيم
    text = strip_formal_punctuation(text)

    return text


