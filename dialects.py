def strip_formal_punctuation(text):
    """
    إزالة علامات الترقيم الرسمية لإنتاج نص بأسلوب واتساب.

    - إزالة كل الفواصل المنقوطة (؛ و ;)
    - إزالة كل النقطتين (: و ：)
    - إزالة كل علامات الحذف (… و ...)
    - إزالة 90% من الفواصل العربية (،)
    - إزالة 80% من النقاط (.)
    - إزالة كل علامات التعجب (! و !) وعلامات الاستفهام (? و ؟)
    """
    # إزالة كاملة: فواصل منقوطة
    text = text.replace('؛', '')
    text = text.replace(';', '')

    # إزالة كاملة: نقطتين
    text = text.replace(':', '')
    text = text.replace('：', '')

    # إزالة كاملة: علامات الحذف
    text = text.replace('…', '')
    text = text.replace('...', '')

    # إزالة كاملة: علامات التعجب والاستفهام
    text = text.replace('!', '')
    text = text.replace('!', '')  # fullwidth
    text = text.replace('?', '')
    text = text.replace('؟', '')

    # إزالة 90% من الفواصل العربية
    result_chars = []
    for ch in text:
        if ch == '،':
            if random.random() < 0.10:
                result_chars.append(ch)
            # else skip
        elif ch == '.':
            if random.random() < 0.20:
                result_chars.append(ch)
            # else skip
        else:
            result_chars.append(ch)
    text = ''.join(result_chars)

    # تنظيف المسافات الزائدة
    text = re.sub(r' {2,}', ' ', text).strip()

    return text


