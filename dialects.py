def apply_saudi_typos(text, intensity='normal'):
    """
    محرك أخطاء الكتابة السعودية الواقعية.

    يطبق الأخطاء بالترتيب:
    1. إسقاط الهمزة (25%)
    2. خلط ة/ه (20%)
    3. خلط ي/ى (15%)
    4. تطويل الحروف (10%)
    5. دمج الكلمات (8%)

    intensity: 'light' | 'normal' | 'heavy'
    """
    if intensity == 'light':
        multiplier = 0.5
    elif intensity == 'heavy':
        multiplier = 1.8
    else:
        multiplier = 1.0

    # ── 1. إسقاط الهمزة (25%) ──
    if random.random() < 0.25 * multiplier:
        hamza_map = [('إ', 'ا'), ('أ', 'ا'), ('ؤ', 'و'), ('ئ', 'ي')]
        pair = random.choice(hamza_map)
        # Replace one random occurrence
        positions = [m.start() for m in re.finditer(re.escape(pair[0]), text)]
        if positions:
            pos = random.choice(positions)
            text = text[:pos] + pair[1] + text[pos + len(pair[0]):]

    # ── 2. خلط ة/ه في آخر الكلمة (20%) ──
    if random.random() < 0.20 * multiplier:
        words = text.split()
        candidates = []
        for idx, w in enumerate(words):
            if w and w[-1] == 'ة':
                candidates.append((idx, 'ة', 'ه'))
            elif w and w[-1] == 'ه' and len(w) > 2:
                candidates.append((idx, 'ه', 'ة'))
        if candidates:
            pick = random.choice(candidates)
            old_word = words[pick[0]]
            words[pick[0]] = old_word[:-1] + pick[2]
            text = ' '.join(words)

    # ── 3. خلط ي/ى في آخر الكلمة (15%) ──
    if random.random() < 0.15 * multiplier:
        words = text.split()
        candidates = []
        for idx, w in enumerate(words):
            if w and w[-1] == 'ي':
                candidates.append((idx, 'ي', 'ى'))
            elif w and w[-1] == 'ى':
                candidates.append((idx, 'ى', 'ي'))
        if candidates:
            pick = random.choice(candidates)
            old_word = words[pick[0]]
            words[pick[0]] = old_word[:-1] + pick[2]
            text = ' '.join(words)

    # ── 4. تطويل حرف وسط الكلمة (10%) ──
    if random.random() < 0.10 * multiplier:
        words = text.split()
        long_words = [(i, w) for i, w in enumerate(words) if len(w) >= 4]
        if long_words:
            idx, w = random.choice(long_words)
            pos = random.randint(1, len(w) - 2)
            words[idx] = w[:pos] + w[pos] + w[pos:]
            text = ' '.join(words)

    # ── 5. دمج كلمات (8%) ──
    if random.random() < 0.08 * multiplier:
        for original, merged in _MERGED_PAIRS:
            if original in text:
                text = text.replace(original, merged, 1)
                break

    return text


def apply_typos(text, probability=0.10):
    """تطبيق خطأ إملائي طبيعي واحد على النص.

    probability: احتمال محاولة الإضافة (الشخصيات تمرّر 1.0 لأن has_typo نفسه نسبته 10%).
    عند المحاولة نضمن خطأً فعلياً: كلمة من قاموس الأخطاء إن وُجدت، وإلا إطالة حرف
    في كلمة طويلة (خطأ شائع طبيعي) — حتى لا تكون الخاصية بلا أثر.

    backward-compatible — يستدعي apply_saudi_typos داخلياً.
    """
    if random.random() > probability:
        return text

    words = text.split()
    if not words:
        return text

    # 1) كلمات لها أخطاء طبيعية معروفة في القاموس
    candidates = [i for i, w in enumerate(words) if w.strip('.,!؟') in NATURAL_TYPOS]
    if candidates:
        i = random.choice(candidates)
        clean = words[i].strip('.,!؟')
        words[i] = words[i].replace(clean, random.choice(NATURAL_TYPOS[clean]))
        text = ' '.join(words)

    # 2) تطبيق محرك الأخطاء السعودي
    text = apply_saudi_typos(text, intensity='light')

    return text


