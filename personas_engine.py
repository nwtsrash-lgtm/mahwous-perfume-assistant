def build_context_hints(persona, product):
    """تنبيهات سياقية (نوع المنتج + إطار الهدية) — مطابقة لمنطق app.py._plan_review.

    تُمكّن أي مستدعٍ (مثل واجهة Streamlit) من نفس تماسك الجنس/الهدية الموجود في
    مسار Flask. تُمرَّر النتيجة كـ extra_block. ترجع '' إن لا تنبيه.
    """
    hints = []
    ptype = product.get('product_type', 'عطر')
    if ptype == 'مكياج':
        hints.append("هذا المنتج عبارة عن مكياج/أدوات تجميل (ليس عطراً). تحدث عن اللون، التغطية، الثبات، أو الملمس، ولا تستخدم كلمات عطرية مثل 'فوحان' أو 'ريحة'.")
    elif ptype == 'معطر_شعر':
        hints.append("هذا المنتج عطر مخصص للشعر. تحدث عن تأثيره على الشعر (ما ينشفه، ريحته تفوح مع الحركة)، ولا تتعامل معه كعطر جسم عادي.")
    elif ptype == 'معطر_جسم':
        hints.append("هذا معطر جسم (Body Mist)، ركز على الانتعاش والترطيب وخفة الريحة بعد الشاور.")
    elif ptype == 'بخور':
        hints.append("هذا بخور/عود. استخدم مصطلحات مثل 'الكسرة'، 'الدخان'، 'التبخير'، 'يمسك في الثياب/المجلس'.")
    elif ptype == 'عناية_جسم':
        hints.append("هذا منتج عناية بالجسم. ركز على الترطيب والنعومة وسرعة الامتصاص.")

    arch_id = persona.get('id', persona.get('archId'))
    is_gift_male = arch_id == 'هدايا_رجل'
    is_gift_female = arch_id == 'هدايا_أنثى'
    if is_gift_male and (product.get('g') == 'نسائي' or ptype == 'مكياج'):
        hints.append("مهم جداً: أنت رجل واشتريت هذا المنتج كهدية (لزوجتك/أمك/أختك). تحدث عن إعجابها بالهدية وردة فعلها، ولا تتحدث كأنك تستخدمه شخصياً!")
    elif is_gift_female and product.get('g') == 'رجالي':
        hints.append("مهم جداً: أنتِ امرأة واشتريتِ هذا المنتج كهدية (لزوجك/أخوكِ/أبوكِ). تحدثي عن فخامة الهدية وكيف عجبته، ولا تتحدثي كأنكِ تستخدمينه شخصياً!")

    # توجيه واعٍ بطبيعة العطر (مناسبته/ثقله) — مطابق لمنطق app.py._plan_review
    if _DEMO_MATCH and not (is_gift_male or is_gift_female):
        _dir = _demo.review_directive(persona, product)
        if _dir:
            hints.append(_dir)

    if not hints:
        return ''
    return "## تنبيهات سياقية (التزم بها بحذافيرها):\n- " + "\n- ".join(hints)


def build_master_prompt(persona, product_name, review_params, used_texts_block='', extra_block=''):
    """بناء البرومبت — الطول من len_target المعاين من بيانات المنافسين.

    عند غياب المعاير (len_target) نتدرّج للسلوك القديم: 4 كلمات بحد أقصى.
    """
    len_target = review_params.get('len_target')
    if len_target:
        length_rule = _rc_directive(len_target)
        examples_note = ('التزم بنفس القصر' if len_target <= 4
                         else 'للأسلوب واللهجة فقط — طولك المطلوب أعلاه مختلف')
    else:
        pattern_info = REVIEW_PATTERNS.get(review_params['pattern'],
                                           REVIEW_PATTERNS.get('ultra_short', {'words': (1, 3)}))
        _min_words, max_words = pattern_info.get('words', (1, 3))
        max_words = min(max_words, 4)  # السلوك القديم: أقصى حد مطلق 4 كلمات
        length_rule = f'من 1 إلى {max_words} كلمات فقط لا أكثر أبداً'
        examples_note = 'التزم بنفس القصر'

    typo_rule = 'أضف خطأ إملائي طبيعي واحد' if persona.get('has_typo') else 'بدون أخطاء إملائية'

    # أمثلة أسلوبية من نصوص المنافسين الحقيقية قرب الطول المستهدف — مرجعية
    # المفردات الفعلية لا مفردات الذكاء. تدرّج آمن للقائمة الثابتة عند غياب الكشط.
    _exemplars = _rc_exemplars(target_len=len_target, n=8)
    examples_block = ('\n'.join(f'- {t}' for t in _exemplars)
                      if _exemplars else _FALLBACK_EXAMPLE_LINES)

    prompt = MASTER_PROMPT.format(
        product_name=product_name,
        rating=review_params['rating'],
        length_rule=length_rule,
        examples_note=examples_note,
        examples_block=examples_block,
        typo_rule=typo_rule,
        used_texts_block=used_texts_block if used_texts_block else '(لا يوجد سابق)',
    )

    return prompt, review_params


