def pick_review_exemplars(gender='male', pattern=None, count=3, used_texts=None):
    """اختيار نماذج تقييمات حسب الجنس والنمط.

    نقطة الدخول الرئيسية — تدعم رجالي ونسائي ومشترك.
    """
    if gender in ('male', 'رجالي'):
        return pick_male_exemplars(pattern, count, used_texts)
    elif gender in ('female', 'نسائي'):
        return pick_female_exemplars(pattern, count, used_texts)
    else:
        # مشترك: خلط بين الاثنين
        half = max(1, count // 2)
        male = pick_male_exemplars(pattern, half, used_texts)
        female = pick_female_exemplars(pattern, count - half, used_texts)
        combined = male + female
        random.shuffle(combined)
        return combined


def get_review_bank_stats():
    """إحصائيات بنك التقييمات"""
    stats = {}
    total_m = 0
    for cat, reviews in ALL_MALE_REVIEWS.items():
        unique = len(set(reviews))
        stats[f'male_{cat}'] = unique
        total_m += unique
    stats['total_unique_male'] = total_m

    total_f = 0
    for cat, reviews in ALL_FEMALE_REVIEWS.items():
        unique = len(set(reviews))
        stats[f'female_{cat}'] = unique
        total_f += unique
    stats['total_unique_female'] = total_f
    stats['grand_total'] = total_m + total_f
    return stats


