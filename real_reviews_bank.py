def pick_male_exemplars(pattern=None, count=3, used_texts=None):
    """اختيار نماذج تقييمات رجالية مناسبة للنمط."""
    if used_texts is None:
        used_texts = set()
    else:
        used_texts = set(used_texts)

    category = PATTERN_TO_MALE_CATEGORY.get(pattern, 'longevity')
    primary_pool = ALL_MALE_REVIEWS.get(category, MALE_LONGEVITY_SILLAGE)

    pool = list(primary_pool)
    other_pools = [v for k, v in ALL_MALE_REVIEWS.items() if k != category]
    for other in other_pools:
        pool.extend(random.sample(other, min(3, len(other))))

    available = [t for t in pool if t not in used_texts]
    if len(available) < count:
        available = pool

    return random.sample(available, min(count, len(available)))


