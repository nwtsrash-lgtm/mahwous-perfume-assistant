    def _generate_single_review(self, product_name, price, category='oriental',
                                 gender='unisex', persona_type=None,
                                 add_typos=False):
        """
        توليد تقييم واحد واقعي بشخصية سعودية محددة
        """
        # اختيار الشخصية واللهجة
        persona = self._pick_persona(gender, persona_type)
        dialect = self._pick_dialect(persona)
        config = self.PERSONA_CONFIG.get(persona, {})
        min_sentences, max_sentences = config.get('sentence_range', (2, 4))
        price_tier = self._classify_price(price)
        rating = self._get_rating()

        # بناء أجزاء التقييم
        parts = []

        # 1) الافتتاحية (دائماً)
        opening = self._pick(self.OPENINGS.get(persona, []))
        if opening:
            parts.append(opening)

        # 2) ذكر اسم المنتج (45%) — لا يبدأ التقييم باسم المنتج أبداً
        # يُضاف فقط بعد افتتاحية موجودة (parts غير فارغة)، وإلا يُسقَط.
        product_mention = self._build_product_mention(product_name, persona)
        if product_mention and parts:
            parts.append(product_mention)

        # 3) وصف الرائحة (80%)
        if random.random() < 0.80:
            scent = self._pick(self.SCENT_TEMPLATES.get(persona, []))
            if scent:
                parts.append(scent)

        # 4) الثبات والفوحان (65%)
        if random.random() < 0.65:
            longevity = self._pick(self.LONGEVITY_TEMPLATES.get(persona, []))
            if longevity:
                parts.append(longevity)

        # 5) تعليق السعر (50%)
        if random.random() < 0.50:
            price_comment = self._get_price_comment(persona, price_tier)
            if price_comment:
                parts.append(price_comment)

        # 6) لمسة شخصية (40%) - هدية/مناسبة/مقارنة/إلخ
        if random.random() < 0.40:
            touch_category = random.choice(list(self.PERSONAL_TOUCHES.keys()))
            touch = self._pick(self.PERSONAL_TOUCHES[touch_category])
            if touch:
                parts.append(touch)

        # 7) نقد بنّاء لتقييمات 3 و 4 نجوم
        if rating <= 4:
            criticism = self._get_criticism(persona, rating)
            if criticism:
                parts.append(criticism)

        # 8) تعجب لهجوي عشوائي (25%)
        if random.random() < 0.25:
            exclaim = self._add_dialect_exclaim(dialect)
            if exclaim:
                # ندرجه بشكل طبيعي
                insert_pos = random.randint(0, max(0, len(parts) - 1))
                parts.insert(insert_pos, exclaim)

        # 9) الخاتمة (75%)
        if random.random() < 0.75:
            closing = self._pick(self.CLOSINGS.get(persona, []))
            if closing:
                parts.append(closing)

        # ── تحديد عدد الجمل النهائي ──
        # انحياز للقِصَر: غالبية تقييمات العملاء الحقيقية قصيرة (جملة–جملتين)
        target_sentences = random.choices([1, 2, max_sentences],
                                          weights=[50, 35, 15], k=1)[0]
        target_sentences = max(1, min(target_sentences, max_sentences))
        if len(parts) > target_sentences + 1:
            # نحتفظ بالافتتاحية والخاتمة ونعمل sample من الوسط
            opening_part = parts[0] if parts else ""
            closing_part = parts[-1] if len(parts) > 1 else ""
            middle = parts[1:-1] if len(parts) > 2 else []
            middle_count = max(0, target_sentences - 2)
            if middle and middle_count > 0:
                selected_middle = random.sample(middle, min(middle_count, len(middle)))
            else:
                selected_middle = []
            parts = [opening_part] + selected_middle
            if closing_part and closing_part != opening_part:
                parts.append(closing_part)

        # ── تجميع النص بفواصل طبيعية ──
        separators_casual = ['، ', '. ', '\n', ' - ', ' ']
        separators_formal = ['، ', '. ', '\n', ' - ']
        separators_elder = ['. ', ' ']

        if persona in ('elder_man', 'elder_woman'):
            seps = separators_elder
        elif persona in ('businessman', 'business_woman', 'perfume_expert', 'beauty_expert'):
            seps = separators_formal
        else:
            seps = separators_casual

        review_text = ""
        for i, part in enumerate(parts):
            if not part:
                continue
            if i == 0:
                review_text = part
            else:
                sep = random.choice(seps)
                review_text += sep + part

        # ── لمسات لهجوية ──
        if random.random() < 0.30:
            review_text = self._apply_dialect_filler(review_text, dialect)

        # ── إيموجي ──
        review_text = self._maybe_add_emoji(review_text, persona)

        # ── أخطاء إملائية (اختياري) ──
        if add_typos:
            review_text = self._apply_typos(review_text)

        # تنظيف المسافات الزائدة
        review_text = re.sub(r' {2,}', ' ', review_text).strip()
        # إزالة فواصل متكررة
        review_text = re.sub(r'(، ){2,}', '، ', review_text)
        review_text = re.sub(r'(\. ){2,}', '. ', review_text)
        # إصلاح أداة الربط "و" قبل فاصل (يمنع "و - ريحته" / "و، ريحته" / "و. ريحته")
        review_text = re.sub(r'\s+و\s*[-–—،.]\s*', ' و ', review_text)
        # إزالة أداة ربط معلّقة في نهاية النص ("... و")
        review_text = re.sub(r'\s+و\s*$', '', review_text).strip()
        # إزالة فاصل/شرطة في بداية النص
        review_text = re.sub(r'^\s*[-–—،.]\s*', '', review_text).strip()

        return {
            'text': review_text,
            'rating': rating,
            'persona_type': persona,
        }

