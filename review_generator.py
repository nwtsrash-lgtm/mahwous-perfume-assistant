    def _pick(self, items):
        """اختيار عنصر عشوائي آمن"""
        if not items:
            return ""
        return random.choice(items)

    def _pick_or_none(self, items, chance=0.5):
        """اختيار عنصر عشوائي بنسبة احتمال معينة"""
        if not items or random.random() > chance:
            return None
        return random.choice(items)

    def _classify_price(self, price):
        """تصنيف السعر"""
        if price is None:
            return 'value'
        price = float(price)
        if price < 80:
            return 'budget'
        elif price <= 200:
            return 'value'
        elif price <= 500:
            return 'premium'
        else:
            return 'luxury'

    def _get_rating(self):
        """تحديد التقييم بالنجوم وفق التوزيع الواقعي"""
        r = random.random()
        if r < 0.70:
            return 5
        elif r < 0.95:
            return 4
        else:
            return 3

    def _pick_persona(self, gender='unisex', persona_type=None):
        """اختيار شخصية عشوائية أو محددة"""
        if persona_type and persona_type in self.ALL_PERSONAS:
            return persona_type

        if gender == 'male':
            return random.choice(self.MALE_PERSONAS)
        elif gender == 'female':
            return random.choice(self.FEMALE_PERSONAS)
        else:
            return random.choice(self.ALL_PERSONAS)

    def _pick_dialect(self, persona_type):
        """اختيار لهجة مناسبة للشخصية"""
        config = self.PERSONA_CONFIG.get(persona_type, {})
        dialects = config.get('dialects', ['najdi'])
        return random.choice(dialects)

    def _apply_dialect_filler(self, text, dialect):
        """إضافة كلمات حشو لهجوية عشوائياً"""
        markers = self.DIALECT_MARKERS.get(dialect, {})
        fillers = markers.get('filler', [])

        if not fillers or random.random() > 0.35:
            return text

        filler = random.choice(fillers)

        # إضافة الحشو بطريقة طبيعية
        insertion_styles = [
            f"{filler} {text}",
            f"{text} {filler}",
        ]

        # أحياناً نضيف في المنتصف
        words = text.split()
        if len(words) > 4:
            mid = len(words) // 2
            words.insert(mid, filler)
            insertion_styles.append(" ".join(words))

        return random.choice(insertion_styles)

    def _add_dialect_exclaim(self, dialect):
        """إضافة تعجب لهجوي"""
        markers = self.DIALECT_MARKERS.get(dialect, {})
        exclaims = markers.get('exclaim', [])
        return self._pick(exclaims) if exclaims else ""

    def _maybe_add_emoji(self, text, persona_type):
        """إضافة إيموجي بناءً على احتمالية الشخصية"""
        config = self.PERSONA_CONFIG.get(persona_type, {})
        emoji_chance = config.get('emoji_chance', 0)

        if random.random() > emoji_chance:
            return text

        emojis = self.EMOJIS.get(persona_type, [])
        if not emojis:
            return text

        emoji = random.choice(emojis)

        # أحياناً في النهاية، أحياناً بعد جملة
        if random.random() < 0.7:
            return f"{text} {emoji}"
        else:
            # إضافة بعد أول جملة
            sentences = re.split(r'([.،!؟])', text, maxsplit=1)
            if len(sentences) >= 2:
                return f"{sentences[0]}{sentences[1]} {emoji} {''.join(sentences[2:])}"
            return f"{text} {emoji}"

    def _apply_typos(self, text):
        """تطبيق أخطاء إملائية شائعة لزيادة الواقعية"""
        for original, variants in self.COMMON_TYPOS.items():
            if original in text and random.random() < 0.15:
                text = text.replace(original, random.choice(variants), 1)
        return text

