# الأنماط القصيرة (≤ ~8 كلمات) — أسلوب العملاء الحقيقي المختصر
SHORT_PATTERNS = ['ultra_short', 'scent_no_name', 'longevity', 'delivery',
                  'season', 'value_focus', 'gift', 'repeat_buy', 'with_note']
# نسبة تفضيل النمط القصير (يُضبط عبر البيئة REVIEW_SHORT_BIAS عند الحاجة)
import os as _os
try:
    SHORT_BIAS = float(_os.environ.get('REVIEW_SHORT_BIAS', '0.80'))
except (TypeError, ValueError):
    SHORT_BIAS = 0.75


def pick_pattern(used_patterns=None, short_bias=None):
    """اختيار نمط موزون مع انحياز للأنماط القصيرة (أسلوب العملاء).

    بنسبة short_bias يُختار النمط من المجموعة القصيرة فقط، وإلا من كل الأنماط.
    """
    if short_bias is None:
        short_bias = SHORT_BIAS
    if short_bias and random.random() < short_bias:
        patterns = [p for p in SHORT_PATTERNS if p in REVIEW_PATTERNS] or list(REVIEW_PATTERNS.keys())
    else:
        patterns = list(REVIEW_PATTERNS.keys())
    weights = [REVIEW_PATTERNS[p]['weight'] for p in patterns]
    if used_patterns:
        for i, p in enumerate(patterns):
            count = used_patterns.get(p, 0)
            if count > 0:
                weights[i] = max(1, weights[i] // (count + 1))
    return random.choices(patterns, weights=weights, k=1)[0]


def pick_pattern_from_category(category, used_patterns=None):
    """اختيار نمط من فئة محددة"""
    cat_patterns = PATTERN_CATEGORIES.get(category, [])
    if not cat_patterns:
        return pick_pattern(used_patterns)
    weights = [REVIEW_PATTERNS[p]['weight'] for p in cat_patterns]
    if used_patterns:
        for i, p in enumerate(cat_patterns):
            count = used_patterns.get(p, 0)
            if count > 0:
                weights[i] = max(1, weights[i] // (count + 1))
    return random.choices(cat_patterns, weights=weights, k=1)[0]


def pick_rating():
    """اختيار تقييم نجوم بناءً على التوزيع الواقعي"""
    ratings = list(RATING_DISTRIBUTION.keys())
    weights = list(RATING_DISTRIBUTION.values())
    return random.choices(ratings, weights=weights, k=1)[0]


def get_pattern_info(pattern_name):
    """معلومات نمط معين"""
    return REVIEW_PATTERNS.get(pattern_name, REVIEW_PATTERNS['ultra_short'])


def get_pattern_description(pattern_name):
    """وصف النمط لإرساله في prompt الـ AI"""
    info = get_pattern_info(pattern_name)
    desc = info['desc']
    words = info['words']
    return f'{desc} ({words[0]}-{words[1]} كلمة)'


def get_ai_directive(pattern_name):
    """التوجيه الاستراتيجي للنمط — يُحقن مباشرة في البرومبت"""
    info = REVIEW_PATTERNS.get(pattern_name, {})
    return info.get('ai_directive', '')


def get_low_rating_reason(rating):
    """سبب التقييم المنخفض"""
    if rating > 3:
        return ''
    reasons = LOW_RATING_REASONS.get(rating, LOW_RATING_REASONS[3])
    return random.choice(reasons)


def get_category_for_pattern(pattern_name):
    """الفئة التي ينتمي إليها النمط"""
    info = REVIEW_PATTERNS.get(pattern_name, {})
    return info.get('category', 'basic')


def get_all_patterns_summary():
    """ملخص كل الأنماط"""
    total_weight = sum(p['weight'] for p in REVIEW_PATTERNS.values())
    summary = []
    for name, info in REVIEW_PATTERNS.items():
        pct = round(info['weight'] / total_weight * 100, 1)
        summary.append({
            'name': name, 'desc': info['desc'], 'weight': info['weight'],
            'percentage': pct, 'words': info['words'], 'category': info['category'],
        })
    return summary


