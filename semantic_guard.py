# -*- coding: utf-8 -*-
"""
semantic_guard.py — الحارس الدلالي (Semantic Guard)
====================================================
يصطاد نزيف الفضاء الكامن (Latent Bleed) والشظايا المبتورة قبل قبول أي تقييم:

1. نزيف دلالي: كلمة من خارج عالم العطور (سيارة/حذاء/مطبخ...) تتسلل للنص.
2. بتر نحوي: جملة تنتهي بأداة معلّقة (لكل/بكل/حق/بس...) — أثر القصّ عند 4 كلمات.

الاستخدام:
    violations = guard_violations(text, max_words=4)   # [] = نظيف
    words = strip_broken_tail(words)                    # إنقاذ أخير بعد القص

stdlib فقط — يستورده guardian.py (فحص ساكن) و app.py (إعادة توليد حيّة) معًا.

ملاحظة تصميم (2026-07-12): جُرِّب حارس «كلمات مخترعة» بمطابقة مفردات الكشط
الشامل (452 نصًّا) — رُفض: 8/30 من golden_sample.json (عبارات سليمة مثل «عطار
يسحر الأنفاس») لأن كلماتها غائبة عن عيّنة صغيرة، لا لأنها مخترعة فعلاً. بلا
قاموس عربي حقيقي يستحيل تمييز «كلمة نادرة سليمة» عن «كلمة مخترعة» آلياً —
عولجت الهلوسة اللغوية عبر تشديد MASTER_PROMPT بدل حارس حتمي كاذب الإيجابية.
"""
import re

# ═══════════════════════════════════════════════════════════
#  القائمة السوداء الدلالية — أسماء من خارج عالم العطور
#  (سيارة/حذاء/مطبخ بأمر العقل المدبّر + نزيف مرصود ميدانيًا)
# ═══════════════════════════════════════════════════════════
SEMANTIC_BLACKLIST = {
    # النزيف المُسمّى في الأمر
    'سيارة', 'سياره', 'سيارتي', 'السيارة', 'السياره', 'بالسيارة', 'بالسياره',
    'حذاء', 'الحذاء', 'جزمة', 'الجزمة', 'جزمه',
    'مطبخ', 'المطبخ', 'مطبخي', 'بالمطبخ',
    # نزيف شائع من فضاءات مجاورة
    'جوال', 'الجوال', 'جوالي', 'شاشة', 'الشاشة', 'لابتوب', 'اللابتوب',
    'تلفزيون', 'التلفزيون', 'غسالة', 'الغسالة', 'ثلاجة', 'الثلاجة',
    'مكيف', 'المكيف', 'كنب', 'الكنب', 'سرير', 'السرير',
    'ملعب', 'الملعب', 'كورة', 'الكورة', 'مباراة', 'المباراة',
    'حارس', 'الحارس',   # مرصود: «يهبل حق حارس» — شظية مبتورة
    'دراجة', 'الدراجة', 'موتر', 'الموتر',
}

# ═══════════════════════════════════════════════════════════
#  أدوات معلّقة — نص ينتهي بواحدة منها = جملة مبتورة (أثر القصّ)
# ═══════════════════════════════════════════════════════════
DANGLING_ENDINGS = {
    'و', 'أو', 'او', 'بس', 'لكن', 'مع', 'في', 'فيه', 'من', 'على', 'عن',
    'إلى', 'الى', 'لي', 'له', 'لها', 'لكل', 'بكل', 'حق', 'اللي', 'الي',
    'إن', 'ان', 'لو', 'إذا', 'اذا', 'بعد', 'قبل', 'فوق', 'تحت', 'عند',
    'مثل', 'غير', 'كل', 'بعض', 'أي', 'اي', 'يا', 'ما', 'ولا', 'إلا', 'الا',
    'عشان', 'علشان', 'لأن', 'لان', 'حتى', 'كأن', 'كان', 'صار', 'ودي', 'يبيله',
}

# سياق استخدام مشروع — لا يُحسب نزيفًا (مرجع للمرشّح العاطفي)
CONTEXT_TOKENS = {
    'مسجد', 'المسجد', 'للمسجد', 'بالمسجد', 'صلاة', 'الصلاة', 'الجمعة', 'جمعة', 'للجمعة',
    'دوام', 'الدوام', 'للدوام', 'بالدوام', 'عمل', 'العمل', 'للعمل', 'مكتب', 'المكتب',
    'سهرة', 'السهرة', 'للسهرة', 'سهرات', 'للسهرات', 'طلعة', 'الطلعة', 'للطلعات', 'طلعات',
    'مشوار', 'مشاوير', 'للمشاوير', 'عزيمة', 'عزايم', 'للعزايم', 'مناسبة', 'مناسبات', 'للمناسبات',
    'عيد', 'العيد', 'للعيد', 'زواج', 'للزواج', 'خطوبة', 'ملكة', 'ليل', 'الليل', 'لليل',
    'صيف', 'الصيف', 'للصيف', 'شتاء', 'الشتاء', 'للشتاء', 'شتوي', 'صيفي',
    'يومي', 'لليومي', 'اليومي', 'جامعة', 'الجامعة', 'للجامعة', 'سفر', 'للسفر',
    'مصعد', 'المصعد', 'مجلس', 'المجلس', 'للمجلس', 'دخلة', 'مقام',
}

# علامات التحفّظ الإيجابي البنّاء (مرجع للمرشّح العاطفي)
RESERVATION_TOKENS = {'بس', 'لكن', 'ودي', 'يبيله', 'توقعت', 'توقعته', 'ياليت', 'يا ليت', 'لولا'}

_WORD_RE = re.compile(r'[ء-ي٠-٩\w]+')


def _tokens(text):
    return _WORD_RE.findall(text or '')


def guard_violations(text, max_words=None):
    """يفحص نصًّا ويرجع قائمة المخالفات — قائمة فارغة تعني نصًّا نظيفًا.

    المخالفات: 'bleed:<كلمة>' نزيف دلالي / 'truncated' بتر نحوي / 'overlength' تجاوز طول.
    """
    v = []
    words = _tokens(text)
    if not words:
        return ['empty']
    for w in words:
        if w in SEMANTIC_BLACKLIST:
            v.append(f'bleed:{w}')
    if words[-1] in DANGLING_ENDINGS:
        v.append('truncated')
    if max_words and len(words) > max_words:
        v.append('overlength')
    return v


def strip_broken_tail(words):
    """إنقاذ أخير بعد القصّ الإجباري: يحذف الذيل المعلّق/النازف حتى تكتمل الجملة.

    يرجع قائمة كلمات لا تنتهي بأداة معلّقة ولا كلمة نازفة (قد تكون كلمة واحدة).
    """
    words = list(words)
    while len(words) > 1 and (words[-1] in DANGLING_ENDINGS or words[-1] in SEMANTIC_BLACKLIST):
        words.pop()
    return words


def has_context(text):
    """هل يذكر النص سياق استخدام (مسجد/دوام/سهرة...)؟ — للمرشّح العاطفي."""
    return any(w in CONTEXT_TOKENS for w in _tokens(text))


def has_reservation(text):
    """هل يحمل النص تحفّظًا إيجابيًا بنّاءً (بس/ودي/يبيله...)؟ — للمرشّح العاطفي."""
    return any(w in RESERVATION_TOKENS for w in _tokens(text))
