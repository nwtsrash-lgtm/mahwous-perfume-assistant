# ═══════════════════════════════════════════════════════════
#  حذف نداء الاسم — محصور حتى لا يمسّ «يا سلام/يا رب/يا ليت/يا هلا»
# ═══════════════════════════════════════════════════════════
_STORE_VOCATIVES = ['صاحبي', 'خوي', 'ربع', 'شيخ', 'جماعة', 'حلفي',
                    'رجال', 'حبيبتي', 'ناس', 'صاح', 'ولد']
_STORE_VOCATIVE_RE = re.compile(
    r'يا\s+(?:' + '|'.join(_STORE_VOCATIVES) + r')(?![ء-ي])')


def strip_store_vocatives(text, persona_name, names=()):
    """حذف حتمي لنداء الاسم من مخرج المتجر: «يا <اسم معروف>» و«يا أبو/أم <كلمة>»
    و«يا <نداء عامّي من قائمة مقصورة>». محصور في `names` + اسم الشخصية + القائمة
    حتى لا يمسّ «يا سلام/يا رب/يا ليت/يا هلا».

    names: مجموعة أسماء معروفة (تمرّر من كل تطبيق — عادةً names.json) لفكّ الاقتران.
    """
    known = set(names)
    parts = (persona_name or '').split()
    if parts:
        known.add(parts[0])
    for nm in known:
        text = re.sub(r'يا\s+' + re.escape(nm) + r'(?![ء-ي])', ' ', text)
    text = re.sub(r'يا\s+(?:أبو|أم)\s+\S+', ' ', text)
    text = _STORE_VOCATIVE_RE.sub(' ', text)  # نداءات عامّية مقصورة
    return re.sub(r'\s+', ' ', text).strip()


# ═══════════════════════════════════════════════════════════
#  طول مُعايَن لكل تقييم — يطابق توزيع العملاء الحقيقي
#  35% قصير جدًا · 40% قصير · 25% متوسط (يكسر رتابة القصة الطويلة)
# ═══════════════════════════════════════════════════════════
LENGTH_BANDS = [
    (0.35, (2, 5),   'قصير جدًا — من كلمتين إلى خمس كلمات فقط، انطباع خاطف بلا قصة'),
    (0.40, (6, 10),  'قصير — من ست إلى عشر كلمات، جملة أو جملتين'),
    (0.25, (11, 18), 'متوسط — من إحدى عشرة إلى ثماني عشرة كلمة، قصة مصغّرة'),
]


def sample_length_target():
    """يعيّن نطاق طول عشوائيًّا وفق التوزيع الحقيقي — يرجع (min, max, وصف نصّي)."""
    r = random.random()
    cum = 0.0
    for prob, (lo, hi), desc in LENGTH_BANDS:
        cum += prob
        if r <= cum:
            return lo, hi, desc
    _, (lo, hi), desc = LENGTH_BANDS[-1]
    return lo, hi, desc


def band_for(hi):
    """اسم النطاق من الحد الأقصى للطول: vshort (≤5) · short (≤10) · story."""
    return 'vshort' if hi <= 5 else ('short' if hi <= 10 else 'story')


